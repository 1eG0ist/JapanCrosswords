    static void Play_level(int lvl_number)
    {
        int str = width_of_unused_sides[lvl_number][1];
        int col = width_of_unused_sides[lvl_number][0];
        int old_str;
        int old_col;
        string lose = @"
 __     __           _                    
 \ \   / /          | |                   
  \ \_/ /__  _   _  | |     ___  ___  ___ 
   \   / _ \| | | | | |    / _ \/ __|/ _ \
    | | (_) | |_| | | |___| (_) \__ \  __/
    |_|\___/ \__,_| |______\___/|___/\___|
";
        string win = @"
 __     __          __          ___       
 \ \   / /          \ \        / (_)      
  \ \_/ /__  _   _   \ \  /\  / / _ _ __  
   \   / _ \| | | |   \ \/  \/ / | | '_ \ 
    | | (_) | |_| |    \  /\  /  | | | | |
    |_|\___/ \__,_|     \/  \/   |_|_| |_|      
";
        bool move = false;
        Console.ForegroundColor = ConsoleColor.Black;
        Console.BackgroundColor = ConsoleColor.White;
        Console.Clear();
        int miss_counter_position = Print_lvl(lvl_number);
        Console.SetCursorPosition(1 + col * 4, str);
        Console.BackgroundColor = background_color_on_levels;
        // highlight first field
        switch (player_tries[lvl_number][str][col])
        {
            case "e":
                Console.ForegroundColor = ConsoleColor.White;
                Console.Write(" ■ ");
                break;

            case "b":
                Console.ForegroundColor = ConsoleColor.Black;
                Console.Write(" ■ ");
                break;

            case "bc":
                Console.ForegroundColor = ConsoleColor.Black;
                Console.Write(" X ");
                break;

            case "rc":
                Console.ForegroundColor = ConsoleColor.Red;
                Console.Write(" X ");
                break;
        }
        
        ValueTuple<Int32, Int32> pos;
        while (true)
        {
            old_str = str;
            old_col = col;
            Console.SetCursorPosition(0, player_tries[lvl_number].Length);
            Console.ForegroundColor = ConsoleColor.White;
            Console.BackgroundColor = ConsoleColor.White;
            switch (Console.ReadKey().Key)
            {
                case ConsoleKey.Spacebar:
                    Console.SetCursorPosition(1 + old_col * 4, old_str);
                    Console.BackgroundColor = background_color_on_levels;
                    if (player_tries[lvl_number][str][col] == "e")
                    {
                        if (levels[lvl_number][str][col] == "e")
                        {
                            player_tries[lvl_number][str][col] = "rc";
                            Console.ForegroundColor = ConsoleColor.Red;
                            count_mistakes[lvl_number]++;
                            Console.Write(" X ");
                            
                            if (!game_mode)
                            {
                                if (count_mistakes[lvl_number] >= 3)
                                {
                                    Console.BackgroundColor = ConsoleColor.White;
                                    Console.ForegroundColor = ConsoleColor.Red;
                                    Console.SetCursorPosition(0, levels[lvl_number].Length + 7);
                                    Console.WriteLine(lose);
                                    Console.WriteLine("(Enter)");
                                    Console.ForegroundColor = ConsoleColor.White;
                                    Clear_all_info_for_lvl(lvl_number);
                                    while (true)
                                    {
                                        if (Console.ReadKey().Key == ConsoleKey.Enter)
                                        {
                                            return;
                                        }
                                    }
                                }
                                pos = Console.GetCursorPosition();
                                Console.ForegroundColor = ConsoleColor.Black;
                                Console.BackgroundColor = ConsoleColor.White;
                                Console.SetCursorPosition(miss_counter_position, 0);
                                Console.Write(count_mistakes[lvl_number] + "  ");
                                Console.SetCursorPosition(pos.Item1, pos.Item2);
                            }
                        }
                        else
                        {
                            player_tries[lvl_number][str][col] = "b";
                            Console.ForegroundColor = ConsoleColor.Black;
                            count_black_fields[lvl_number][1]++;
                            if (count_black_fields[lvl_number][0] == count_black_fields[lvl_number][1])
                            {
                                done_levels.Add(lvl_number);
                                Console.SetCursorPosition(1 + old_col * 4, old_str);
                                Console.BackgroundColor = ConsoleColor.White;
                                switch (player_tries[lvl_number][old_str][old_col])
                                {
                                    case "e":
                                        Console.ForegroundColor = ConsoleColor.White;
                                        Console.Write(" ■ ");
                                        break;

                                    case "b":
                                        Console.ForegroundColor = ConsoleColor.Black;
                                        Console.Write(" ■ ");
                                        break;

                                    case "bc":
                                        Console.ForegroundColor = ConsoleColor.Black;
                                        Console.Write(" X ");
                                        break;

                                    case "rc":
                                        Console.ForegroundColor = ConsoleColor.Red;
                                        Console.Write(" X ");
                                        break;
                                }
                                Console.BackgroundColor = ConsoleColor.White;
                                Console.ForegroundColor = ConsoleColor.Green;
                                Console.SetCursorPosition(0, levels[lvl_number].Length + 7);
                                Console.WriteLine(win);
                                Console.WriteLine("(Enter)");
                                Console.ForegroundColor = ConsoleColor.White;
                                Clear_all_info_for_lvl(lvl_number);
                                while (true)
                                {
                                    if (Console.ReadKey().Key == ConsoleKey.Enter)
                                    {
                                        return;
                                    }
                                }
                            }
                            Console.Write(" ■ ");
                        }
                    }
                    move = false;
                    break;

                case ConsoleKey.Backspace:
                    Console.SetCursorPosition(1 + old_col * 4, old_str);
                    Console.BackgroundColor = background_color_on_levels;
                    if (player_tries[lvl_number][str][col] == "e")
                    {
                        player_tries[lvl_number][str][col] = "bc";
                        Console.ForegroundColor = ConsoleColor.Black;
                        Console.Write(" X ");
                    }
                    else if (player_tries[lvl_number][str][col] == "bc")
                    {
                        player_tries[lvl_number][str][col] = "e";
                        Console.ForegroundColor = ConsoleColor.White;
                        Console.Write(" ■ ");
                    }
                    break;

                case ConsoleKey.M:
                    pos = Console.GetCursorPosition();
                    Console.ForegroundColor= ConsoleColor.Black;
                    Console.SetCursorPosition(miss_counter_position, 0);
                    if (game_mode)
                    {
                        game_mode = !game_mode;
                        Console.WriteLine(count_mistakes[lvl_number] + "  ");
                    }
                    else
                    {
                        game_mode = !game_mode;
                        Console.WriteLine("inf");
                    }
                    Console.SetCursorPosition(pos.Item1, pos.Item2);
                    move = false;
                    break;

                case ConsoleKey.A:
                    if (str == width_of_unused_sides[lvl_number][1] && col == width_of_unused_sides[lvl_number][0])
                    {
                        str = levels[lvl_number].Length - 1;
                        col = levels[lvl_number][0].Length - 1;
                    }
                    else if (col == width_of_unused_sides[lvl_number][0])
                    {
                        str--;
                        col = levels[lvl_number][0].Length - 1;
                    }
                    else
                        col--;
                    move = true;
                    break;

                case ConsoleKey.D:
                    if (str == levels[lvl_number].Length - 1 && col == levels[lvl_number][0].Length - 1)
                    {
                        str = width_of_unused_sides[lvl_number][1];
                        col = width_of_unused_sides[lvl_number][0];
                    }
                    else if (col == levels[lvl_number][0].Length - 1)
                    {
                        str++;
                        col = width_of_unused_sides[lvl_number][0];
                    }
                    else
                        col++;
                    move = true;
                    break;

                case ConsoleKey.W:
                    if (str == width_of_unused_sides[lvl_number][1])
                        str = levels[lvl_number].Length - 1;
                    else
                        str--;
                    move = true;
                    break;

                case ConsoleKey.S:
                    if (str == levels[lvl_number].Length - 1)
                        str = width_of_unused_sides[lvl_number][1];
                    else
                        str++;
                    move = true;
                    break;

                case ConsoleKey.C:
                    Clear_field(lvl_number);
                    Print_lvl(lvl_number);
                    break;

                case ConsoleKey.Escape:
                    Console.BackgroundColor = ConsoleColor.Black;
                    Console.ForegroundColor = ConsoleColor.White;
                    Console.Clear();
                    int which = 0;
                    Console.WriteLine("Ku");
                    
                    while (true)
                    {
                        Console.ForegroundColor = ConsoleColor.White;
                        Console.SetCursorPosition(0, 0);
                        if (which == 0)
                        {
                            Console.BackgroundColor = background_color_on_levels;
                            Console.WriteLine("Сохранить и выйти");
                            Console.BackgroundColor = ConsoleColor.Black;
                            Console.WriteLine("Выйти");
                        } 
                        else
                        {
                            Console.BackgroundColor = ConsoleColor.Black;
                            Console.WriteLine("Сохранить и выйти");
                            Console.BackgroundColor = background_color_on_levels;
                            Console.WriteLine("Выйти");
                        }
                        Console.BackgroundColor = ConsoleColor.Black;
                        Console.WriteLine("(Enter) to select, (S) to move");
                        Console.ForegroundColor = ConsoleColor.Black;
                        switch (Console.ReadKey().Key)
                        {
                            case ConsoleKey.Enter:
                                if (which == 0)
                                    return;
                                else
                                {
                                    Clear_all_info_for_lvl(lvl_number);
                                    return;
                                }
                                break;

                            case ConsoleKey.S:
                                if (which == 0)
                                    which = 1;
                                else
                                    which = 0;
                                break;
                        }
                    }
                    break;
            }


            // if was move action, reset background colors
            if (move)
            {
                // Reset old position (from Yellow to Black if move)
                Console.SetCursorPosition(1 + old_col * 4, old_str);
                Console.BackgroundColor = ConsoleColor.White;
                switch (player_tries[lvl_number][old_str][old_col])
                {
                    case "e":
                        Console.ForegroundColor = ConsoleColor.White;
                        Console.Write(" ■ ");
                        break;

                    case "b":
                        Console.ForegroundColor = ConsoleColor.Black;
                        Console.Write(" ■ ");
                        break;

                    case "bc":
                        Console.ForegroundColor = ConsoleColor.Black;
                        Console.Write(" X ");
                        break;

                    case "rc":
                        Console.ForegroundColor = ConsoleColor.Red;
                        Console.Write(" X ");
                        break;
                }

                // Reset new position (from Black to Yellow if move)
                Console.SetCursorPosition(1 + col * 4, str);
                Console.BackgroundColor = background_color_on_levels;
                switch (player_tries[lvl_number][str][col])
                {
                    case "e":
                        Console.ForegroundColor = ConsoleColor.White;
                        Console.Write(" ■ ");
                        break;

                    case "b":
                        Console.ForegroundColor = ConsoleColor.Black;
                        Console.Write(" ■ ");
                        break;

                    case "bc":
                        Console.ForegroundColor = ConsoleColor.Black;
                        Console.Write(" X ");
                        break;

                    case "rc":
                        Console.ForegroundColor = ConsoleColor.Red;
                        Console.Write(" X ");
                        break;
                }
            }
        }
        
    }

    static int Print_lvl(int number_of_lvl)
    {
        Console.Clear();
        bool flag = true;
        for (int i = 0; i < player_tries[number_of_lvl].Length; i++)
        {
            Console.ForegroundColor = ConsoleColor.Black;
            Console.Write("| ");
            for (int j = 0; j < player_tries[number_of_lvl][i].Length; j++)
            {
                if (player_tries[number_of_lvl][i][j] == "e")
                {
                    Console.ForegroundColor = ConsoleColor.White;
                    Console.Write("■");
                } 
                else if (player_tries[number_of_lvl][i][j] == "b")
                {
                    Console.ForegroundColor = ConsoleColor.Black;
                    Console.Write("■");
                } 
                else if (player_tries[number_of_lvl][i][j] == "bc")
                {
                    Console.ForegroundColor = ConsoleColor.Black;
                    Console.Write("X");
                } 
                else if (player_tries[number_of_lvl][i][j] == "rc")
                {
                    Console.ForegroundColor = ConsoleColor.Red;
                    Console.Write("X");
                }
                else
                {
                    Console.ForegroundColor = ConsoleColor.Black;
                    Console.Write(player_tries[number_of_lvl][i][j]);
                    if (player_tries[number_of_lvl][i][j].Length > 1)
                        flag = false;
                }
                Console.ForegroundColor = ConsoleColor.Black;
                if (flag)
                    Console.Write(" | ");
                else
                {
                    Console.Write("| ");
                    flag = true;
                }
            }
            Console.WriteLine();
        }
        Console.WriteLine();
        int miss_counter_position_on_string = player_tries[number_of_lvl][0].Length * 4 + 5;
        Console.WriteLine("Нажмите M, чтобы сменить режим игры");
        Console.WriteLine("Нажмите Space, чтобы закрасить ячейку");
        Console.WriteLine("Нажмите Backspace, чтобы исключить ячейку или удалить исключение");
        Console.WriteLine("Нажмите C, чтобы очистить всё");
        Console.WriteLine("Нажмите Esc, чтобы выйти");
        Console.SetCursorPosition(miss_counter_position_on_string, 0);
        Console.Write($"Ошибки: {count_mistakes[number_of_lvl]}");
        return miss_counter_position_on_string + 8;
    }

    static void Clear_all_info_for_lvl(int level_number)
    {
        for (int i = 0; i < player_tries[level_number].Length; i++)
        {
            for (int j = 0; j < player_tries[level_number][i].Length; j++)
            {
                if (levels[level_number][i][j] == "b")
                    player_tries[level_number][i][j] = "e";
                else 
                    player_tries[level_number][i][j] = levels[level_number][i][j];
            }
        }
        count_mistakes[level_number] = 0;
        count_black_fields[level_number][1] = 0; 
    }

    static void Clear_field(int level_number)
    {
        for (int i = 0; i < player_tries[level_number].Length; i++)
        {
            for (int j = 0; j < player_tries[level_number][i].Length; j++)
            {
                 if (player_tries[level_number][i][j] == "bc" || player_tries[level_number][i][j] == "b")
                {
                    player_tries[level_number][i][j] = "e";
                }
            }
        }
    }
