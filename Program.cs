                    else if (coord_x == 0)
                    {
                        coord_x = levels_menu[0].Length - 1;
                        coord_y--;
                    }
                    else
                        coord_x--;
                    break;

                case ConsoleKey.D:
                    if (coord_x == levels_menu[0].Length-1 && coord_y == levels_menu.Length - 1)
                    {
                        coord_x = 0;
                        coord_y = 0;
                    } else if (coord_x == levels_menu[0].Length - 1)
                    {
                        coord_x = 0;
                        coord_y++;
                    } else
                        coord_x++;
                    break;

                case ConsoleKey.W:
                    if (coord_y == 0)
                        coord_y = levels_menu.Length - 1;
                    else coord_y--;
                    break;

                case ConsoleKey.S:
                    if (coord_y == levels_menu.Length - 1)
                        coord_y = 0;
                    else coord_y++;
                    break;
            }
            

            Console.SetCursorPosition(1 + old_x * 5, 1 + old_y * 2);
            Console.BackgroundColor = ConsoleColor.Black;
            if (progress["open"].Contains(levels_menu[old_y][old_x]))
            {
                if (done_levels.Contains(levels_menu[old_y][old_x]))
                    Console.ForegroundColor = ConsoleColor.Green;
                else
                    Console.ForegroundColor = ConsoleColor.Blue;
            } 
            else
            {
                Console.ForegroundColor = ConsoleColor.Red;
            }
            if (levels_menu[old_y][old_x] < 10)
                Console.Write(" ");
            Console.Write($" {levels_menu[old_y][old_x]} ");


            Console.SetCursorPosition(1+coord_x*5, 1+coord_y*2);
            Console.BackgroundColor = ConsoleColor.Yellow;
            if (progress["open"].Contains(levels_menu[coord_y][coord_x]))
            {
                if (done_levels.Contains(levels_menu[coord_y][coord_x]))
                    Console.ForegroundColor = ConsoleColor.Green;
                else
                    Console.ForegroundColor = ConsoleColor.Blue;
            } 
            else
            {
                Console.ForegroundColor = ConsoleColor.Red;
            }
            if (levels_menu[coord_y][coord_x] < 10)
                Console.Write(" ");
            Console.Write($" {levels_menu[coord_y][coord_x]} ");

            Console.ForegroundColor = ConsoleColor.Black;
            Console.BackgroundColor = ConsoleColor.Black;
        }
    }

    static void Print_levels_menu(int x, int y)
